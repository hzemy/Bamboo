package com.bamboo.demo.Models;

public class Activity {
    private String id;
    private String type;
    private int calories;
    private int minutes;

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public int getCalories() {
        return calories;
    }

    public int getMinutes() {
        return minutes;
    }
}
